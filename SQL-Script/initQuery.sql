-- Crear la base de datos ContribuyentesDB
CREATE DATABASE ContribuyentesDB;
USE ContribuyentesDB;

-- Crear la tabla Contribuyentes
CREATE TABLE Contribuyentes (
    RncCedula NVARCHAR(50) PRIMARY KEY,
    Nombre NVARCHAR(100),
    Tipo NVARCHAR(50),
    Estatus NVARCHAR(50)
);

-- Crear la tabla ComprobantesFiscales
CREATE TABLE ComprobantesFiscales (
    Id INT PRIMARY KEY IDENTITY,
    RncCedula NVARCHAR(50),
    NCF NVARCHAR(50),
    Monto DECIMAL(18, 2),
    Itbis18 DECIMAL(18, 2),
    FOREIGN KEY (RncCedula) REFERENCES Contribuyentes(RncCedula)
);


